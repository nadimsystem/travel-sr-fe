let currentLanguage = 'id'; // Default set to Indonesian

function updateLanguage(lang) {
    currentLanguage = lang;
    localStorage.setItem('sutanraya_lang', lang);
    
    // Update active state indication could be added here if needed
    
    const elements = document.querySelectorAll('[data-translate]');
    elements.forEach(element => {
        const key = element.getAttribute('data-translate');
        if (translations[lang] && translations[lang][key]) {
             if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
                element.placeholder = translations[lang][key];
            } else {
                element.innerHTML = translations[lang][key];
            }
        }
    });

    // Dispatch event
    window.dispatchEvent(new CustomEvent('languageChanged', { detail: { language: lang } }));
}

function changeLanguage(lang) {
    updateLanguage(lang);
}

document.addEventListener('DOMContentLoaded', () => {
    // Set default to Indonesian (Explicit)
    const savedLang = localStorage.getItem('sutanraya_lang') || 'id';
    console.log('Initializing language:', savedLang);
    updateLanguage(savedLang);
});

